/**
 * @brief Returns the minimum value on an arry
 *
 * @param arr Array
 * @param arrLength Array Length
 * @return int Minimum value
 */
int arrayMinimum(int arr[], int arrLength);

/**
 * @brief Returns the sum of the even numbers on the array
 *
 * @param arr Array
 * @param arrLength Array Length
 * @return int Sum of the even numbers
 */
int arraySumEven(int arr[], int arrLength);

/**
 * @brief Binary search algorithm
 *
 * @param val Value to search
 * @param arr Array of integers
 * @param start Start index
 * @param end End index
 * @return int Index of the element
 */
int binarySearch(int val, int arr[], int start, int end);

/**
 * @brief Quick sort algorithm
 *
 * @param arr
 * @param low
 * @param high
 */
void quickSort(int arr[], int low, int high);